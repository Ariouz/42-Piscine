int	ft_sqrt(int nb)
{
	int	sqrt;

	sqrt = 0;
	while (sqrt * sqrt < nb && sqrt < 46340)
	{
		sqrt++;
	}
	if (sqrt * sqrt == nb)
		return (sqrt);
	return (0);
}
